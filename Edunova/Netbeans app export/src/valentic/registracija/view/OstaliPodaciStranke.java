    private void dodajPodatkeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dodajPodatkeActionPerformed
        

        if (!ku.provjeraUnosaZipa(zip) || !ku.provjeraUnosaNaziva(regija) || !ku.provjeraUnosaNaziva(drzava) || !ku.provjeraUnosaNaziva(grad) || !ku.provjeraUnosaAdrese(adresa2) || !ku.provjeraUnosaZaDatum(datumrodjenja) || !ku.provjeraUnosaZaMobitel(drugimobitel) || !ku.provjeraUnosFiksnog(fiksnitelefon) || !ku.provjeraUnosaZaMail(email)) {

            return;
        }

        postaviPoljaOstalihPodatakaStranke(stranka);

        if (ispravnoNe.isSelected()) {
            JOptionPane.showMessageDialog(
                    rootPane,
                    "Ukoliko želite dodati ostalepodatke označite ispravnost odabira!",
                    "Pogreška",
                    JOptionPane.ERROR_MESSAGE);
        } else {
            if (obradaGlavniProzor.dodajOstaleStranke(stranka)) {

                JOptionPane.showMessageDialog(
                        rootPane,
                        "Uspješno dodani podaci! ",
                        "Poruka",
                        JOptionPane.PLAIN_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(
                        rootPane,
                        "Pogreška kod dodavanja podataka!",
                        "Greška",
                        JOptionPane.ERROR_MESSAGE);
            }
        }
        // TODO add your handling code here:

    }//GEN-LAST:event_dodajPodatkeActionPerformed

