    void popuniPolja(Stranka stranka) {

        if (stranka.getAdresa2() == null) {
            adresa2.setText("");
        } else {
            adresa2.setText(stranka.getAdresa2());
        }

        if (stranka.getDrugiMobitel() == null) {
            drugimobitel.setText("");
        } else {
            drugimobitel.setText(stranka.getDrugiMobitel());
        }

        if (stranka.getFiksniTelefon() == null) {
            fiksnitelefon.setText("");
        } else {
            fiksnitelefon.setText(stranka.getFiksniTelefon());
        }

        if (stranka.getEmail() == null) {
            email.setText("");
        } else {
            email.setText(stranka.getEmail());
        }

        if (stranka.getDrzava() == null) {
            drzava.setText("");
        } else {
            drzava.setText(stranka.getDrzava());
        }

        if (stranka.getGrad() == null) {
            grad.setText("");
        } else {
            grad.setText(stranka.getGrad());
        }

        if (stranka.getZip() == 0) {
            zip.setText("");
        } else {
            zip.setText(Integer.toString(stranka.getZip()));
        }

        if (stranka.getRegija() == null) {
            regija.setText("");
        } else {
            regija.setText(stranka.getRegija());
        }

        if (stranka.getDatumRodjenja() == null) {
            datumrodjenja.setText("");
        } else {
            datumrodjenja.setText(sdf.format(stranka.getDatumRodjenja()));
        }

    }

    void setStranka(Stranka stranka) {
        this.stranka = stranka;
    }

    private void postaviPoljaOstalihPodatakaStranke(Stranka stranka) {

        DateFormat format = sdf;
        try {
            stranka.setDatumRodjenja(format.parse(datumrodjenja.getText()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    rootPane,
                    "Datum mora biti u formatu (dd.mm.yyyy)!",
                    "Pogreška",
                    JOptionPane.ERROR_MESSAGE);
            return;

        }

        try {
            stranka.setAdresa2(adresa2.getText());
        } catch (Exception e) {
            stranka.setAdresa2(null);
        }
        try {
            stranka.setDrugiMobitel(drugimobitel.getText());
        } catch (Exception e) {
            stranka.setDrugiMobitel(null);
        }

        try {
            stranka.setFiksniTelefon(fiksnitelefon.getText());
        } catch (Exception e) {
            stranka.setFiksniTelefon(null);
        }

        try {
            stranka.setEmail(email.getText());
        } catch (Exception e) {
            stranka.setEmail(null);
        }

        try {
            stranka.setDrzava(drzava.getText());
        } catch (Exception e) {
            stranka.setDrzava(null);
        }

        try {
            stranka.setGrad(grad.getText());
        } catch (Exception e) {
            stranka.setGrad(null);
        }

        try {
            stranka.setZip(Integer.parseInt(zip.getText()));
        } catch (Exception e) {
            stranka.setZip(0);
        }

        try {
            stranka.setRegija(regija.getText());
        } catch (Exception e) {
            stranka.setRegija(null);
        }
    }

   

